package cadenas;

public class Cadenas {

	public static void main(String[] args) {

		String nombre = "Carlos";

		System.out.println("Longitud de la cadena de caracteres: " + nombre.length());

		System.out.println("La tercera letra del nombre es " + nombre.charAt(2));

		System.out.println("Si cambio las 's' por 't' el nombre queda: " + nombre.replace('s', 't'));

	}

}
