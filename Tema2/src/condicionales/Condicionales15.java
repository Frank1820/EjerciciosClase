package condicionales;

public class Condicionales15 {

	public static void main(String[] args) {

		// Mostramos los 10 primeros números pares,
		// empezando por 2.

		// La variable i actuará como contador.
		int i = 1;

		while (i <= 10) {

			System.out.println(2 * i);
			i++;
			
		}

	}

}








