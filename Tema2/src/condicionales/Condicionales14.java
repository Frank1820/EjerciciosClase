package condicionales;

public class Condicionales14 {

	public static void main(String[] args) {
		
		// Mostramos los 10 primeros números pares,
		// empezando por 2.
		
		// La variable i actuará como acumulador.
		int i = 2;
		
		while(i<=20){
			System.out.println(i);
			i = i + 2;		// i += 2;
		}

	}

}
