package condicionales;

public class Condicionales16 {

	public static void main(String[] args) {

		// Mostramos los 100 primeros números impares,
		// empezando por 1.

		int i = 1;

		while (i <= 100) {

			System.out.println(2 * i - 1);
			i++;
		}
		
		System.out.println("Ahí quedan.");

	}

}






