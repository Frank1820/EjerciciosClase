
public class Operaciones {

	public static void main(String[] args) {

		int a = 7;
		int b = 3;

		int suma = a + b;
		int resta = a - b;
		int mult = a * b;
		int div = a / b;

		System.out.println(suma);
		System.out.println(resta);
		System.out.println(mult);
		System.out.println(div);

		int resto = a % b;
		System.out.println(resto);
	}

}
