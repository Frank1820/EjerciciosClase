
public class CaracteresDeControl {

	public static void main(String[] args) {

		// Saltos de línea
		System.out.println("Tu \nnombre es: \nCarlos");

		// Tabulaciones
		System.out.println("1020 \t1202 \t212 \t1021 \t3278");
		System.out.println("6277 \t2399 \t2388 \t2781 \t2178");

		// Comillas
		System.out.println("Su nota podría considerarse \"mejorable\" ");
		System.out.println("Su nota podría considerarse \'mejorable\' ");

		// Contrabarras
		System.out.println("La ruta de la carpeta usuarios es C:\\Users");
	}

}
